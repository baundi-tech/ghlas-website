export type LandRecord = typeof RECORDS[number]

export function fmtCoords(lat: number, lng: number) {
  return `${Math.abs(lat).toFixed(4)}° ${lat >= 0 ? 'N' : 'S'},  ${Math.abs(lng).toFixed(4)}° ${lng >= 0 ? 'E' : 'W'}`
}

export function searchRecords(type: string, query: string): LandRecord[] {
  const q = query.trim().toLowerCase()
  if (!q) return []
  switch (type) {
    case 'owner':     return RECORDS.filter(r => r.name.toLowerCase().includes(q))
    case 'ghanaCard': return RECORDS.filter(r => r.ghanaCard.toLowerCase() === q)
    case 'district':  return RECORDS.filter(r => r.district.toLowerCase().includes(q) || r.region.toLowerCase().includes(q))
    case 'location':  return RECORDS.filter(r => r.district.toLowerCase().includes(q) || r.region.toLowerCase().includes(q))
    default:          return []
  }
}
